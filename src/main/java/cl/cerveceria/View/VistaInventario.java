    private void setearDatosInventario(){
        inventario.setIdinventario(calcularIdInventario());
        inventario.setProductoIdProducto(calcularProducto());
        inventario.setStock(Integer.parseInt(this.lblStockActualSet.getText()) +  Integer.parseInt(this.txtAgregarStock.getText()));
    }
    
    
    private Producto calcularProducto(){
        List ls;
        ls = productoController.findProductoEntities();
        for (int i = 0; i < ls.size(); i++) {
            producto = (Producto)ls.get(i);
            if (producto.getIdProducto() == Integer.valueOf(this.lblIdProdSet.getText())) {
                return producto;
            }
        }
        return producto;
    }
    
    private void setearDatosMovimiento(){
        movimiento.setCantidadMovimiento(Integer.parseInt(this.txtAgregarStock.getText()));
        movimiento.setFechaMovimiento(new Date());
        movimiento.setProductoIdProducto(calcularProducto());
        movimiento.setTipoMovimiento("Ingreso");
    }
    
