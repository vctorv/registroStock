    public void getTablaTotalInventario(){
        String col[] = {"ID","MARCA CERVEZA","TIPO CERVEZA","FORMATO","GRADO ETILICO","PRECIO COSTO","PRECIO VENTA","STOCK"};
        DefaultTableModel tablaVista = new DefaultTableModel(col, 0);
        Object[] tablaBD = new Object[8];
        List ls;
        try {
            ls = inventarioController.findInventarioEntities();
            for(int i = 0; i < ls.size(); i++){
                inventario = (Inventario)ls.get(i);
                tablaBD[0] = inventario.getIdinventario();
                tablaBD[1] = inventario.getProductoIdProducto().getNombreProducto();
                tablaBD[2] = inventario.getProductoIdProducto().getTipoProducto();
                tablaBD[3] = inventario.getProductoIdProducto().getFormato();
                tablaBD[4] = inventario.getProductoIdProducto().getGradoEtilico();
                tablaBD[5] = inventario.getProductoIdProducto().getCosto();
                tablaBD[6] = inventario.getProductoIdProducto().getPrecioVenta();
                tablaBD[7] = inventario.getStock();
                tablaVista.addRow(tablaBD);
            }
            this.tblStock.setModel(tablaVista);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Error al cargar datos");
        }
    }

    
    
