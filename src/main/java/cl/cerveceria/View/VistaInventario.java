    private int calcularIdInventario(){
        int numId = 0;
        List ls;
        try {
            ls = inventarioController.findInventarioEntities();
            for (int i = 0; i < ls.size(); i++) {
                inventario = (Inventario)ls.get(i);
                if (inventario.getIdinventario() == Integer.valueOf(this.lblIdProdSet.getText())) {
                    numId = inventario.getIdinventario();
                    return numId;
                }
            }
        } catch (Exception e) {
        }
        return numId;
    }
    
    private Producto calcularProductoCompra(){
        List ls;
        ls = productoController.findProductoEntities();
        for (int i = 0; i < ls.size(); i++) {
            producto = (Producto)ls.get(i);
            if (producto.getIdProducto() == Integer.valueOf(this.lblIdProd.getText())) {
                return producto;
            }
        }
        return producto;
    }
    
